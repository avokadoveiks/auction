/**
 * Архетипы поведения ботов
 * Каждый архетип определяет стиль игры бота
 */

/**
 * Turtle - осторожный игрок
 * Характеристики:
 * - Медленные реакции (2.2-4.8 сек)
 * - Не всегда делает ставку (25% шанс)
 * - Экономит деньги (минимальный резерв 6)
 */
export function makeTurtle() {
  return {
    name: "Turtle",
    baseDelayMsRange: [2200, 4800],   // медленные реакции
    bidChance: 0.25,                  // не всегда ставит
    minReserve: 6                     // не лезет в последний доллар
  };
}
